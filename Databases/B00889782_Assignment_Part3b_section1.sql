-- create the database projectdb
DROP DATABASE IF EXISTS projectdb; -- if it already exists, drop it so we can make a new one
CREATE DATABASE projectdb; -- create the database
USE projectdb; -- select that database to use

DROP TABLE IF EXISTS awards; -- if the awards table exists, drop it so we can reset the database
DROP TABLE IF EXISTS movie_has_people; -- if the linker table exists, drop it to reset the database
DROP TABLE IF EXISTS people; -- if people table exists, drop it to reset the database
DROP TABLE IF EXISTS movie; -- if movie table exists, drop it so we can reset th database

-- create tables for projectdb

-- create the movie table
CREATE TABLE IF NOT EXISTS movie (
-- create the attributes
	movie_id INT AUTO_INCREMENT, -- represents the generated primary key
    movie_title VARCHAR(100) UNIQUE, -- represents the title of the movie
    release_date VARCHAR(50), -- when the movie was released
	budget INT, -- movie budget in USD
    mpaa_rating VARCHAR(50), -- film mpaa rating
    genre VARCHAR(100), -- movie genre
    production_cost INT, -- production cost formovie is USD
    -- set the primary key
    PRIMARY KEY (movie_id)
    );

-- create the awards table
CREATE TABLE IF NOT EXISTS awards (
-- create the attributes
	award_id INT AUTO_INCREMENT UNIQUE, -- generated primary key fr each award
    movie_id INT NOT NULL, -- movie id representing a specific movie from the movie table
    award_type VARCHAR(100), -- represents the award that was considerd
    nominations INT, -- number of award nominations
    wins INT, -- number of award wins
    CHECK (nominations >= 0 AND wins >= 0), -- check that no negative numbers were inserted
    -- set the primary and foreign keys
    PRIMARY KEY (award_id),
    FOREIGN KEY (movie_id) REFERENCES movie(movie_id)
    );

-- create the people table
CREATE TABLE IF NOT EXISTS people (
-- create the attributes
	person_id INT AUTO_INCREMENT UNIQUE, -- generated primary key for each person
    person_name VARCHAR(100) UNIQUE NOT NULL, -- the name of each person
    person_role VARCHAR(100), -- what role they had in the making of the movie
    -- set the primary key
    PRIMARY KEY (person_id)
    );
    
-- create the linker table
CREATE TABLE IF NOT EXISTS movie_has_people (
-- create the attributes
	movie_id INT NOT NULL, -- represents the movie associated with the id
    people_id INT NOT NULL, -- the people who were involved in this film
    -- set the primary and foreign keys
    PRIMARY KEY (movie_id, people_id),
    FOREIGN KEY (movie_id) REFERENCES movie(movie_id),
    FOREIGN KEY (people_id) REFERENCES people(person_id)
    );
    
